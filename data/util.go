// hexToByte converts the the first two hex bytes of x into a byte.
func hexToByte(x string) (byte, bool) {
	b1 := hexValues[x[0]]
	b2 := hexValues[x[1]]
	return (b1 << 4) | b2, b1 != 255 && b2 != 255
}
